package org.oops;

public class super_1 {
    super_1()
    {
        System.out.println("Parent Default constructor");
    }

    super_1(int a)
    {
        System.out.println("Parent one parameter Constructot");
    }
    super_1(int a, int b)
    {
        System.out.println("Parent two parameter constructor");
    }

    super_1(int a, int b, int c)
    {
        System.out.println("Parent Three parameter constructor");
    }

    void method1()
    {
        System.out.println(" Parent Default method");
    }

    void method1(int a)
    {
        System.out.println(" Parent one parameter method");
    }

    void method1(int a, int b)
    {
        System.out.println(" Parent two parameter method");
    }

    void method1(int a, int b,int c)
    {
        System.out.println(" Parent three parameter method");
    }
}
